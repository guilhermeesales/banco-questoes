    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String nome = inputNome.getText();
        String email = inputEmail.getText();
        
        
        if(jtable.getSelectedRow() != -1) {
            Integer id = (int) jtable.getValueAt(jtable.getSelectedRow(), 0);
            ControlProfessor cprof = new ControlProfessor();
            cprof.editarProfessor(id, nome, email);
            DefaultTableModel modelo = (DefaultTableModel) jtable.getModel();
            modelo.setNumRows(0);
            readJTable();
                    
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecione algum professor da tabela.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

