    private void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jtable.getModel();
        
        UsuarioDAO userDAO = new UsuarioDAO();
        
        for(Usuario user: userDAO.Read()) {
            modelo.addRow(new Object[]{
                user.getId(),
                user.getNome(),
                user.getEmail()
           
            });
                        
        }     
    }
    
    private void readJTableForName(String nome) {
        
        DefaultTableModel modelo = (DefaultTableModel) jtable.getModel();
        
        ControlProfessor cp = new ControlProfessor();
        
        for(Usuario user: cp.consultarProfessor(nome)) {
            modelo.addRow(new Object[]{
                user.getId(),
                user.getNome(),
                user.getEmail()
                
           
            });
            
            
        } 
         
    }
